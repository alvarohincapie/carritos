package geniar.siscar.util;

public class FileUtilsErrorException extends Exception {

	private static final long serialVersionUID = 1L;

	public FileUtilsErrorException() {
	}

	public FileUtilsErrorException(String arg0) {
		super(arg0);
	}

	public FileUtilsErrorException(Throwable arg0) {
		super(arg0);
	}

	public FileUtilsErrorException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
